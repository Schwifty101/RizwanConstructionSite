// Polyfill Response.json for Node.js < 18 compatibility
if (!global.Response.json) {
  global.Response.json = function(data, init = {}) {
    return new Response(JSON.stringify(data), {
      ...init,
      headers: {
        'Content-Type': 'application/json',
        ...init.headers,
      },
    })
  }
}

